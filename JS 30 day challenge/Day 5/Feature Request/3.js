/* Concatenation Function Script: Write a script that includes a function
expression to concatenate two strings & returns the result */

const concatenate = function(str1, str2) {
  return str1 + str2;
};

// Example usage
console.log("The concatenated string is:", concatenate("Hello", "World")); // Output: The concatenated string is: HelloWorld
console.log("The concatenated string is:", concatenate("Ayush", "Singh")); // Output: The concatenated string is: AyushSingh
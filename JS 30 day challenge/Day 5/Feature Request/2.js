/* Square Calculation Function Script: Create a script that includes
a function to calculate the square of a number & returns the result. */

function square(a) {
  return a * a;
}

// Example usage
console.log("The square of 5 is", square(5));   // Output: The square of 5 is 25
console.log("The square of -3 is", square(-3)); // Output: The square of -3 is 9
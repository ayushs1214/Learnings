/* Access and log a specific element from a two-dimeansional Array. */

// Create a two-dimensional array (matrix)
const matrix = [
  [1, 2, 3],
  [4, 5, 6],
  [7, 8, 9]
];

// Access and log a specific element
// For example, log the element at row 2, column 1 (which is 8)
const row = 2;
const column = 1;
const specificElement = matrix[row][column];

console.log(specificElement); // Output: 8

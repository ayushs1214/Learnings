/*Declare a variable using LET, assign it an initial value, reassign
it a new value, and then print the value of the variable to the console.*/

// Declare a variable using let and assign it an initial value
let myVariable = "FIRST VALUE";

// Log the initial value to the console
console.log("Initial value:", myVariable);

// Reassign a new value to the variable
myVariable = "New Assigned";

// Log the new value to the console
console.log("Reassigned Value:", myVariable);
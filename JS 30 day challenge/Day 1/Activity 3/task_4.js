/*Create a variable of different data types (Number, Boolean, strings,
object, array) and log each variable's type using the TYPEOF Operator*/

const myNumber = 42; // number
const myString = "Hello, world!"; // string
const myBoolean = true; // boolean
const myObject = { name: "Alice", age: 30 }; // object
const myArray = [1, 2, 3, 4, 5]; // array

console.log(typeof myNumber);   // Output: number
console.log(typeof myString);   // Output: string
console.log(typeof myBoolean);  // Output: boolean
console.log(typeof myObject);   // Output: object
console.log(typeof myArray);    // Output: object